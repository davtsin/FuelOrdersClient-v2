    // update companies in ComboBox list and companies Set in holder object
    private void updateCompanyCBList() {
        tableHolder.setCompanies(companyDao.getCompanies());
        jComboBox1.setModel(new CompanyCBModel(tableHolder.getCompanies()));
    }

    // update gas stations ComboBox list and gas stations Set belonging
    // to the selected company in holder object
    private void updateGasStationCBList() {
        String selectedCompanyName = jComboBox1.getSelectedItem().toString();
        // find company by name with Java 8 agreagate operatioin
        Company company = tableHolder.getCurrentCompany(selectedCompanyName);
        tableHolder.setCompanyGasStations(gasStationDao.getGasStationsByCompanyId(company.getId()));
        jComboBox2.setModel(new GasStationCBModel(tableHolder.getCompanyGasStations()));
    }

    private static String convertDate(String dateStr) {
        LocalDate date = LocalDate.parse(dateStr,
            DateTimeFormatter.ofPattern("dd'.'MM'.'yyyy"));
        return date.toString();
    }

