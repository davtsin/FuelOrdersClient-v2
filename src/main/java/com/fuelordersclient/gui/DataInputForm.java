    // create new gas station in table GAS_STATIOIN
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String companyName = jComboBox1.getSelectedItem().toString();
        Company company = tableHolder.getCurrentCompany(companyName);
        String code = jTextField2.getText();
        String address = jTextField3.getText();
        gasStationDao.addGasStation(new GasStation(company, code, address));
        updateGasStationCBList();
        jComboBox2.setSelectedItem(code + "#" + address);
    }//GEN-LAST:event_jButton3ActionPerformed

