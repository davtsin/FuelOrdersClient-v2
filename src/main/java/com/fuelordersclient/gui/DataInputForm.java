    // create new order in table FUEL_ORDER
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String codeAndAddress = jComboBox2.getSelectedItem().toString();
        GasStation gasStation = tableHolder.getCurrentGasStation(codeAndAddress);

        String orderDate = convertDate(jTextField4.getText());
        String gasType = jComboBox3.getSelectedItem().toString() + " "
            + jTextField9.getText();
        double costPerLiter = Double.valueOf(jTextField5.getText());
        double discount = 
            jCheckBox1.isSelected() ? Double.valueOf(jTextField6.getText()) : 0;
        double amountLiters = Double.valueOf(jTextField7.getText());
        double totalCost = Double.valueOf(jTextField8.getText());

        Order order = new Order(gasStation, orderDate, gasType, costPerLiter,
            discount, amountLiters, totalCost);
        orderDao.addOrder(order);
    }//GEN-LAST:event_jButton2ActionPerformed

